    print_step "–û—á–∏—Å—Ç–∫–∞ –≤—Ä–µ–º–µ–Ω–Ω—ã—Ö —Ñ–∞–π–ª–æ–≤..."
    
    # –£–¥–∞–ª—è–µ–º –≤—Ä–µ–º–µ–Ω–Ω—ã–µ —Ñ–∞–π–ª—ã pip
    if dir_exists "/tmp"; then
        find /tmp -name "pip-*" -type d -exec rm -rf {} + 2>/dev/null || true
    fi
    
    # –£–¥–∞–ª—è–µ–º –∫—ç—à pip
    if dir_exists "~/.cache/pip"; then
        rm -rf ~/.cache/pip 2>/dev/null || true
    fi
    
    print_success "–í—Ä–µ–º–µ–Ω–Ω—ã–µ —Ñ–∞–π–ª—ã –æ—á–∏—â–µ–Ω—ã"
}

